public class SwitchCase {

    public static void main(String args[]) {

        int num = 200;

        switch (num) {
            case 100:
                System.out.println("case1 ");
            case 200:
                System.out.println("case2 ");
                break;
            case 300:
                System.out.println("case3 ");
            case 400:
                System.out.println("case4 ");
        }
    }

}
