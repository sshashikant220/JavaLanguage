public class Array2 {

    public static void main(String[] args) {

        int num[] = new int[4];
        num[0] = 5;
        num[1] = 10;
        num[2] = 15;
        num[3] = 20;
        for (int i = 0; i < 3; i++) {
            System.out.print(num[i] + " ");
        }

    }

}
